package in.com.Collection;

public class Emp1 {
	int id ;
	String name;
	String phone;
	public Emp1(int id, String name, String phone) {
		super();
		this.id = id;
		this.name = name;
		this.phone = phone;
	}
	@Override
	public String toString() {
		return "Emp1 [id=" + id + ", name=" + name + ", phone=" + phone + "]";
	}
	
	

}
